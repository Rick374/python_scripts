
# Cantidad de segundos en un día
segundos_por_minuto = 60
minutos_por_hora = 60
horas_por_dia = 24
segundos_en_un_dia = segundos_por_minuto * minutos_por_hora * horas_por_dia

# Calcular segundos en una semana
segundos_en_una_semana = segundos_en_un_dia * 7

# Imprimir el resultado
print("Segundos en un día: ", segundos_en_un_dia)
print("Segundos en una semana: ", segundos_en_una_semana)
