nombre = "Ricardo"
apellido = "Gallo"

largo_nombre = len(nombre)
largo_apellido = len(apellido)

suma_largos = largo_nombre + largo_apellido

print("La suma de los largos de nombre y apellido es:", suma_largos)

nombre_completo = nombre+apellido

largo_nombre_completo = len(nombre_completo)

diferencia_largos = largo_nombre_completo - suma_largos

print("La diferencia de largos es:", diferencia_largos)
